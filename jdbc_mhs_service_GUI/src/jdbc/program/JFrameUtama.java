    public JFrameUtama() {
        initComponents();
        ListId.setModel(listModelId);
        ListNama.setModel(listModelNama);
        tampilkanSemuaData();
        
        // Listener untuk mendeteksi klik pada listId atau listNama
        ListSelectionListener selectionListener = (ListSelectionEvent e) -> {
            if (e.getValueIsAdjusting() || syncingSelection) {
                return;
            }

            syncingSelection = true;
            int index = ((javax.swing.JList<?>) e.getSource()).getSelectedIndex();
            if (index >= 0) {
                ListId.setSelectedIndex(index);
                ListNama.setSelectedIndex(index);
                isiKolomPilihan(index);
            }
            syncingSelection = false;
        };

        ListId.addListSelectionListener(selectionListener);
        ListNama.addListSelectionListener(selectionListener);
    }

    private void tampilkanSemuaData() {
        List<Mahasiswa> list = service.getAll();
        listModelId.clear();
        listModelNama.clear();

        for (Mahasiswa mhs : list) {
            listModelId.addElement(String.valueOf(mhs.getId()));
            listModelNama.addElement(mhs.getNama());
        }
    }

    private void isiKolomPilihan(int index) {
        if (index < 0 || index >= listModelId.size()) {
            return;
        }

        String idText = listModelId.getElementAt(index);
        String namaText = listModelNama.getElementAt(index);

        txtEditId.setText(idText);
        txtEditNama.setText(namaText);
        txtHapusId.setText(idText);
        txtHapusNama.setText(namaText);
    }

    private void bersihkanInput() {
        txtInputNama.setText("");
        txtEditId.setText("");
        txtEditNama.setText("");
        txtHapusId.setText("");
        txtHapusNama.setText("");
        ListId.clearSelection();
        ListNama.clearSelection();
    }

    private int hitungIdBerikutnya() {
        int maxId = 0;
        for (int i = 0; i < listModelId.getSize(); i++) {
            Integer id = parseId(listModelId.getElementAt(i));
            if (id != null && id > maxId) {
                maxId = id;
            }
        }
        return maxId + 1;
    }

    private Integer parseId(String text) {
        if (text == null) {
            return null;
        }
        String trimmed = text.trim();
        if (trimmed.isEmpty()) {
            return null;
        }
        try {
            return Integer.valueOf(trimmed);
        } catch (NumberFormatException ex) {
            return null;
        }
    }   
 


