    private void btnTambahkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahkanActionPerformed
        String nama = txtInputNama.getText();
        if (nama == null || nama.trim().isEmpty()) {
            return;
        }

        int nextId = hitungIdBerikutnya();
        Mahasiswa mhs = new Mahasiswa(nextId, nama.trim());
        service.add(mhs);
        txtInputNama.setText("");
        tampilkanSemuaData();      
    }//GEN-LAST:event_btnTambahkanActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        Integer id = parseId(txtEditId.getText());
        if (id == null) {
            return;
        }

        String nama = txtEditNama.getText();
        if (nama == null || nama.trim().isEmpty()) {
            return;
        }

        Mahasiswa mhs = new Mahasiswa(id, nama.trim());
        service.update(mhs);
        bersihkanInput();
        tampilkanSemuaData();     
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        Integer id = parseId(txtHapusId.getText());
        if (id == null) {
            return;
        }

        service.delete(id);
        bersihkanInput();
        tampilkanSemuaData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnResetIndeksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetIndeksActionPerformed
        service.indexReset();
        bersihkanInput();
        tampilkanSemuaData();
    }//GEN-LAST:event_btnResetIndeksActionPerformed

